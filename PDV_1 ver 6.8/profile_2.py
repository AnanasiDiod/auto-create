import ezdxf as dxf
from point import *


def profile(width, heigh, quantity, path=str(), ver=False):
    doc = dxf.new()
    doc.layers.add("FIGURE", color=2)
    msp = doc.modelspace()
    p = point()
    p.msp = msp

    p.go_line(321.28, 0)
    p.set_xy(318.78, 2.5)
    p.go_arc(8, 2.5)
    p.go_arc(7, 2.5)
    p.set_xy(318.78, 2.5)
    p.go_line(-47.1, 0)
    p.go_arc(4, 5)
    p.go_arc(3, 5)
    p.go_line(0, 16)
    p.go_arc(2, 5)
    p.go_arc(1, 5)
    p.go_line(78.44, 0)
    p.go_arc(8, 5)
    p.go_arc(7, 5)
    p.go_line(0, -(446 + (width - 400)))
    p.go_arc(6, 5)
    p.go_arc(5, 5)
    p.go_line(-78.44, 0)
    p.go_arc(4, 5)
    p.go_arc(3, 5)
    p.go_line(0, 16)
    p.go_arc(2, 5)
    p.go_arc(1, 5)
    p.go_line(47.1, 0)
    p.set_xy(321.28, -(399 + (width - 400)))
    p.go_arc(6, 2.5)
    p.go_arc(5, 2.5)
    p.set_xy(321.28, -(399 + (width - 400)))
    p.go_line(-321.28, 0)
    p.set_xy(2.5, -(401.51 + (width - 400)))
    p.go_arc(4, 2.5)
    p.go_arc(3, 2.5)
    p.set_xy(2.5, -(401.51 + (width - 400)))
    p.go_line(47.1, 0)
    p.go_arc(8, 5)
    p.go_arc(7, 5)
    p.go_line(0, -16)
    p.go_arc(6, 5)
    p.go_arc(5, 5)
    p.go_line(-78.44, 0)
    p.go_arc(4, 5)
    p.go_arc(3, 5)
    p.go_line(0, 446 + (width - 400))
    p.go_arc(2, 5)
    p.go_arc(1, 5)
    p.go_line(78.44, 0)
    p.go_arc(8, 5)
    p.go_arc(7, 5)
    p.go_line(0, -16)
    p.go_arc(6, 5)
    p.go_arc(5, 5)
    p.go_line(-47.1, 0)
    p.set_xy(0, 0)
    p.go_arc(2, 2.5)
    p.go_arc(1, 2.5)

    # отверстия в уголках

    p.set_xy(17.6, 15.5)
    p.circle(0, 0, 4.9/2)
    p.circle(0, -(430 + (width - 400)), 4.9/2)
    p.circle(27, 0, 4.9/2)
    p.circle(0, 430 + (width - 400), 4.9/2)

    p.set_xy(276.68, 15.5)
    p.circle(0, 0, 4.9/2)
    p.circle(0, -(430 + (width - 400)), 4.9/2)
    p.circle(27, 0, 4.9/2)
    p.circle(0, 430 + (width - 400), 4.9/2)

    # проушины под крепление к воздуховоду и решёткам

    # верхний левый угол
    p.set_xy(-10.84, 8.78)
    p.go_angle(135, 10)
    p.go_arc(3, 4.5)
    p.go_arc(2, 4.5)
    p.go_arc(1, 4.5)
    p.go_arc(8, 4.5)
    p.go_angle(-45, 10)
    p.go_arc(7, 4.5)
    p.go_arc(6, 4.5)
    p.go_arc(5, 4.5)
    p.go_arc(4, 4.5)

    # верхний правый угол
    p.set_xy(325.76, 15.15)
    p.go_angle(45, 10)
    p.go_arc(1, 4.5)
    p.go_arc(8, 4.5)
    p.go_arc(7, 4.5)
    p.go_arc(6, 4.5)
    p.go_angle(-135, 10)
    p.go_arc(5, 4.5)
    p.go_arc(4, 4.5)
    p.go_arc(3, 4.5)
    p.go_arc(2, 4.5)

    # нижний левый угол
    p.set_xy(-17.92, -(414.85 + (width - 400)))
    p.go_angle(45, 10)
    p.go_arc(1, 4.5)
    p.go_arc(8, 4.5)
    p.go_arc(7, 4.5)
    p.go_arc(6, 4.5)
    p.go_angle(-135, 10)
    p.go_arc(5, 4.5)
    p.go_arc(4, 4.5)
    p.go_arc(3, 4.5)
    p.go_arc(2, 4.5)

    # нижний правый угол
    p.set_xy(332.83, -(421.22 + (width - 400)))
    p.go_angle(135, 10)
    p.go_arc(3, 4.5)
    p.go_arc(2, 4.5)
    p.go_arc(1, 4.5)
    p.go_arc(8, 4.5)
    p.go_angle(-45, 10)
    p.go_arc(7, 4.5)
    p.go_arc(6, 4.5)
    p.go_arc(5, 4.5)
    p.go_arc(4, 4.5)

    # крепление к профилю 1
    p.set_xy(39.44, -15.6)
    p.circle(0, 0, 4.9/2)
    p.circle(242.4, 0, 4.9/2)
    p.circle(0, -(367.8 + (width - 400)), 4.9/2)
    p.circle(-242.4, 0, 4.9/2)
    # крепление уголков
    if ver:
        p.set_xy(54.94, -48.5)
    else:
        p.set_xy(116.34, -48.5)
    p.circle(0, 0, 4.9/2)
    p.circle(0, -(151 + (width - 400)/2), 4.9/2)
    p.circle(0, -(151 + (width - 400)/2), 4.9/2)
    # метка
    if ver:
        p.set_xy(-30.94, -(203 + (width - 400)/2))
        p.go_arc(4, 1.5)
        p.go_arc(3, 1.5)
        p.go_arc(2, 1.5)
        p.go_arc(1, 1.5)
        p.go_arc(4, 2.02)
        p.go_arc(3, 2.02)
        p.go_arc(2, 2.02)
        p.go_arc(1, 2.02)
        p.go_line(1.5, 0)
        p.go_line(0, -7)
        p.go_line(-0.75, 0)
        p.go_line(0, 6.21)
        p.go_line(-0.75, 0)
        p.set_xy(-30.94, -(203 + (width - 400)/2))
        p.go_line(0, 0.75)
        p.go_arc(4, 1.5/2)
        p.go_arc(3, 1.5/2)
        p.go_arc(2, 1.5/2)
        p.go_arc(1, 1.5/2)
        p.go_line(0, 1.46)
        p.go_arc(4, 2.54/2)
        p.go_arc(3, 2.54/2)
        p.go_arc(2, 2.54/2)
        p.go_arc(1, 2.54/2)
        doc.saveas(path+"Профиль 2 верх "+str(width)+'x' +
                   str(heigh)+' 0,8мм ' + str(quantity)+'шт.dxf')
    else:
        doc.saveas(path+"Профиль 2 низ "+str(width)+'x' +
                   str(heigh)+' 0,8мм ' + str(quantity)+'шт.dxf')


def main(width, heigh, quantity, path=str()):
    profile(width, heigh, quantity, path, False)
    profile(width, heigh, quantity, path, True)
